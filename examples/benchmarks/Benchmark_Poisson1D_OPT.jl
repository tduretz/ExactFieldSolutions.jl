Residual!(F, T, Ta, b, k, Δx, x)    = Residual_OPT!(F, T, Ta, b, k, Δx, x)

function main(Δx, L)

    @show ncx = Int64(L / Δx)

    # Parameters
    x   = (min=-L/2, max=L/2)
    xc  = LinRange(x.min+Δx/2., x.max-Δx/2., ncx)
    xv  = LinRange(x.min, x.max, ncx+1)

    # Allocations
    T   = zeros(ncx+1)
    b   = zeros(ncx+1)
    k   =  ones(ncx+1)
    F   = zeros(ncx+1)
    δT  = zeros(ncx+1)
    Ta  = zeros(ncx+1)

    for i in eachindex(k)
        Ta[i]   = 𝑢(xv[i])
        k[i]    = β(xv[i])
        b[i]    =-∇𝑞(xv[i])
    end

    T .= Ta

    # Sparsity pattern
    input       = rand(ncx+1)
    output      = similar(input)
    Res_closed! = (F, T) -> Residual!(F, T, Ta, b, k, Δx, x)
    sparsity    = Symbolics.jacobian_sparsity(Res_closed!, output, input)
    J           = Float64.(sparse(sparsity))
    colors      = matrix_colors(J)

    # Newton iterations
    for iter=1:10

        # Residual evaluation: T is found if F = 0
        Residual!(F, T, Ta, b, k, Δx, x)
        Res_closed! = (F, T) -> Residual!(F, T, Ta, b, k, Δx, x)
        r = norm(F)/ncx
        @printf("## Iteration %06d: r = %1.2e ##\n", iter, r)
        if r < 1e-10 break end
            
        # Jacobian assembly
        forwarddiff_color_jacobian!(J, Res_closed!, T, colorvec = colors)

        # Solve
        δT   .= .-J\F

        # update
        T    .+= δT
    end


    # # Evaluate exact solution
    # for i in eachindex(T)
    #     sol   = Poisson1D_VarCoeff([xc[i]] )
    #     Ta[i] = sol.u
    # end

    p1=plot(xlabel="x", ylabel="u", title="Solution")
    # p1=plot!(xv, T.-Ta, label="difference")
    p1=plot(xv, Ta, label="analytics")
    p2=plot(xv, k, title="coefficient")
    display(plot(p1, p2))

    # Error
    return mean(abs.(T .- Ta))
end

